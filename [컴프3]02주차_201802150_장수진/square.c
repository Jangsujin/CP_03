#include <stdio.h>

int main() {
	float f; // 입력받을 실수
	printf("실수를 입력하세요 : "); // 실수 입력 안내문
	scanf_s("%f", &f, 4); // float 형태로 입력받는다.
	printf("제곱 :%15.6e\n", f*f);
	// 제곱, 필드폭 15, 오른쪽 정렬, 소수점은 여섯번째 자리까지만, 지수형태로 출력. 
	printf("세제곱 :%15.6e\n", f*f*f);
	// 세제곱, 필드폭 15, 오른쪽 정렬, 소수점은 여섯번째 자리까지만, 지수형태로 출력.

	return 0;

}